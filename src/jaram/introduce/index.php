<h2>자람은 1985년 전자계산학과 선배님들에 의해 생겨난 전산 전공 학회입니다.</h2>

<p>
1996년에 전자컴퓨터 공학부로 학부 통합된 이후 지금은 전자컴퓨터 공학부 후배들이 계속 그 명맥을 이어 가고 있고 2005년 현재 21기까지 이르게 되었습니다.
</p>


<p>
학회 이름 자람(Jaram)은 '잘하다' 라는 의미와 '자라다' 라는 두 가지 뜻을 내포하고 있습니다.<br />
전산 분야에서 '최고 전문가가 되자' 라는 당찬 포부와 함께 변화무쌍한 전산분야인 만큼 '현실에 안주하지 말고 계속 정진하자' 라는 뜻도 담겨 있습니다.
</p>

<div class="quote">
자람은 전산, 즉 컴퓨터 공학(Computer Engineering)을 공부하는 모임입니다.
</div>

<p>
기본적으로 프로그래밍 언어(Programming Language), 자료구조(Data Structure), 알고리즘(Algorithm) 과 같은 기초 학문을 익히고 다양하게는 데이터베이스(Database), 운영체제(Operating System), 인공지능(Artificial Inteligent), 웹 서비스(Web Service), 네트워크(Network) 등의 전반적인 분야를 공부하고 있습니다.<br />
분야가 워낙 방대하다 보니 세부적인 내용들을 모두 공부하지는 못하지만 매주 2회 있는 세미나를 통해서 전산 분야 각각에 대한 기본 배경 지식을 익히고 관심 있는 사람들끼리 스터디 조직을 구성하여 심화학습을 하고 있습니다.
</p>
<p>
이론적인 공부에만 그치는 게 아니라 공부했던 내용을 토대로 작품을 만들어서 자체 전시회 및 공학대전 학술제에 출품하고 있습니다.<br />
작년(2002년도) 학술제에 참가해서 은상을 수상하였으며 올해(2003년도) 1학기에는 자체 전시회를 개최하기도 하였습니다.
이번 공학대전 「Know Limit? No Limit!」에서도 순위권을 목표로.. 그리고 교수님과 학부생 들에게는 더 가까이 다가가는 학회가 되기 위해 열심히 준비를 하였습니다.<br />
아직은 공부를 하는 학생들이라 개개인 작품에 미흡한 점이 많지만 많은 분들이 구경 오셔서 즐겁게 보고 가셨으면 합니다.
</p>
<p>
이외에도 자람은 세미나와 스터디, 전시회 등의 학술적인 활동뿐만 아니라 MT, 여름여행, 체육대회, OB-YB의 만남 등 가족 모임을 통해서 '자람 가족' 간의 정을 돈독하게 만들어 가고 있습니다.
</p>

<p>
자람 홈페이지(<a href="http://jaram.org" target="_blank">http://jaram.org</a>)는 1998년도에 처음 기획된 이후 계속적인 리뉴얼 작업을 거치면서 현재의 모습을 갖추게 되었습니다.<br />
자람 홈페이지는 처음 기획 때부터 자람 웹 팀의 자체 기술만으로 제작되어 왔습니다.<br />
2002년도부터는 홈페이지와는 별도로 스터디 페이지(<a href="http://study.jaram.org" target="_blank">http://study.jaram.org</a>)도 운영하면서 스터디 조직을 통한 학회원들의 스터디 결과물을 쌓아가고 있습니다.
</p>
